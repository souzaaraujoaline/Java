public class DoisNumeros {
    public static void main(String[] args) {
        //Declarei os valores que eu quero
        int a = 12;
        int b = 51;

        System.out.println("Os números são 12 e 51");
        
        int sum = a + b;
        System.out.println("Sum: " + sum);
    
        int difference = a - b;
        System.out.println("Difference: " + difference);

        int product = a * b;
        System.out.println("Product: " + product); // vai aparecer a mensagem product + o resultado da operação

        int quotient = a / b;
        System.out.println("Quotient: " + quotient); // vai aparecer a mensagem product + o resultado da operação
    }
}
