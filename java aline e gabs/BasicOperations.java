public class BasicOperations {
    public static void main(String[] args) {
        //Declarei os valores que eu quero
        int a = 5;
        int b = 3;

        int sum = a + b; //vai fazer a soma
        int difference = a - b; //vai fazer a subtração
        int product = a * b; //vai fazer a multiplicação
        int quotient = a / b; //vai fazer a divisão

        System.out.println("Sum: " + sum); //vai aparecer a mensagem sum + o resultado esperado
        System.out.println("Difference: " + difference); //vai aparecer a mensagem difference + o resultado esperado
        System.out.println("Product: " + product); // vai aparecer a mensagem product + o resultado da operação
        System.out.println("Quotient: " + quotient); // vai aparecer a mensagem quotient + o resultado da operação
    }
}
