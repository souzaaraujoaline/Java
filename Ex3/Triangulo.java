package Ex3;

public class Triangulo {
    public static void main(String[] args) {

        int a = 3;
        int b = 4;
        int c = 9;
        int d = 16; 

        int product = a * a;
        System.out.println("O quadrado de 3 é " + product);

        int product2 = b * b;
        System.out.println("O quadrado de 4 é " + product2);

        int sum = c + d;
        System.out.println("O valor da hipotenusa é " + sum);
}
}
